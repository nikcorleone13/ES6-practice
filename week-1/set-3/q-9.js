// Write an ES6 function to return the second element of the given 
// array by adding “Hello” before it.

// Your ES6 code here

const sayHello = inputArray => 'Hello ' + inputArray[1];

console.log(sayHello(["Akshay", "Sweta", "Prerana", "Vinay"]))
// Hello Sweta

console.log(sayHello(["Kanika", "Rakesh", "Prerana", "Puja"]))
// Hello Rakesh